package br.ufu.gustavodejesus.trabalho.model;

public class Brand {
    private String name;
    private int code;

    public Brand(int code, String name) {
        this.code = code;
        this.name = name;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
